public class While {

    public static void main(String args[]){
      // while
        int myVariable = 1;

        while (myVariable <= 10){
            System.out.println(myVariable);
            myVariable++;
        }
        System.out.println("i am out of loop");

           // do while loop
            do {
                System.out.println(myVariable);
                myVariable++;

            } while(myVariable < 10);



    }
}
